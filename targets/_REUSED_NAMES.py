"""
==============================
_DISALLOWED_MINOR_PLANET_NAMES
==============================

This is a maintained set of names that have been used for a satellite or comet and also a
minor planet.

When one of these names is interpreted as a target, it is always interpreted as the
satellite, never as the associated minor planet, unless additional information indicates
that the target is understood to refer to the minor planet.
"""

